// ============================================================
// AI 岗位匹配器专用 — 结构化简历证据摘要
// ============================================================
// 用途：供千问 3.6 进行快速岗位匹配，减少 token、提升响应速度。
// 与 resumeForAI.md（完整简历）互补：
//   - 快速匹配：JD + resumeMatchProfile → MatchResult
//   - 深度分析：JD + resumeMatchProfile + resumeForAI.md → MatchResult
//
// 原则：
//   1. 证据优先 — 每条能力/领域都有简历原文证据
//   2. 不编造 — 所有内容均可在 resumeForAI.md 中找到对应依据
//   3. 可迁移表达 — 不完全相同的场景用「具备迁移性」表达
//   4. 适合模型读取 — 结构化、短句化、多事实、少修饰
// ============================================================

// ---- 类型定义 ----------------------------------------------------------------

export type MatchDimensionKey =
  | "businessScenario"
  | "productCapability"
  | "projectExperience"
  | "bonusPotential";

export interface CandidateProfile {
  name: string;
  targetRole: string;
  roleSummary: string;
  yearsOfExperience: string;
}

export interface Positioning {
  title: string;
  summary: string;
  targetKeywords: string[];
}

export interface BusinessDomain {
  name: string;
  keywords: string[];
  evidence: string[];
  relatedProjects: string[];
}

export interface CapabilityEvidence {
  capability: string;
  keywords: string[];
  evidenceProjects: string[];
  evidence: string[];
  supportsJDRequirements: string[];
}

export interface WorkExperienceEvidence {
  company: string;
  role: string;
  period: string;
  businessContext: string;
  responsibilities: string[];
  capabilityKeywords: string[];
  evidenceSentences: string[];
}

export interface ProjectEvidence {
  name: string;
  role: string;
  businessContext: string;
  problem: string;
  productActions: string[];
  outcomes: string[];
  keywords: string[];
  supportsDimensions: MatchDimensionKey[];
  evidenceSentences: string[];
}

export interface BonusSignal {
  name: string;
  keywords: string[];
  description: string;
  evidence: string[];
  supportsJDRequirements: string[];
}

export interface EducationEvidence {
  school: string;
  major: string;
  degree: string;
  period: string;
}

export interface ScoringHint {
  dimension: MatchDimensionKey;
  strongEvidence: string[];
  weakEvidence: string[];
}

export interface ResumeMatchProfile {
  candidate: CandidateProfile;
  positioning: Positioning;
  businessDomains: BusinessDomain[];
  coreCapabilities: CapabilityEvidence[];
  workExperiences: WorkExperienceEvidence[];
  projectEvidences: ProjectEvidence[];
  bonusSignals: BonusSignal[];
  education: EducationEvidence[];
  scoringHints: ScoringHint[];
}

